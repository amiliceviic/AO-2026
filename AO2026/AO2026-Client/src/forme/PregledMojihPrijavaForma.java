    private void btnIzmeniPrijavuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniPrijavuActionPerformed
        // TODO add your handling code here:
        int row = tabela.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Greska! Morate selektovati prijavu koju zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        } else {
            PrijavaVolontiranja p = lista.get(row);
            
            if (p.getStatusPrijave() != StatusPrijave.U_OBRADI) {
                JOptionPane.showMessageDialog(this, "Greska! Moguće je menjati samo prijave koje su maksimalno 24h do datuma smene!", "Greska", JOptionPane.ERROR_MESSAGE);
            }
            else {
                new IzmenaPrijaveZaVolontiranjeForma(p, lista, this).setVisible(true);
            }
        }
    }//GEN-LAST:event_btnIzmeniPrijavuActionPerformed

    private void btnOtkaziPrijavuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOtkaziPrijavuActionPerformed
        // TODO add your handling code here:
        int row = tabela.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Greska! Morate selektovati prijavu koju zelite da otkazete!", "Greska", JOptionPane.ERROR_MESSAGE);
        } else {
            PrijavaVolontiranja p = lista.get(row);
            
            if (p.getStatusPrijave() != StatusPrijave.U_OBRADI) {
                JOptionPane.showMessageDialog(this, "Greska! Moguće je menjati samo prijave u obradi!", "Greska", JOptionPane.ERROR_MESSAGE);
            }
            else {
                
                int izbor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da otkažete prijavu?", "Potvrda", JOptionPane.YES_NO_OPTION);
                
                if(izbor == JOptionPane.YES_OPTION) {
                    KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.OTKAZ_PRIJAVE, p);
                    
                    Komunikacija.getInstance().posaljiZahtev(kz);
                    
                    ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
                    
                    if(so.getPoruka().toUpperCase().equals("USPESNO OBRISANA PRIJAVA")) {
                        obrisiFajlKlijenta(p);
                        
                        lista.remove(row);
                        popuniTabelu();
                        
                        JOptionPane.showMessageDialog(this, "Uspesno obrisana prijava!", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                    }
                    else {
                        JOptionPane.showMessageDialog(this, "Greska! Greska pri brisanju prijave za volontiranj!", "Greska", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnOtkaziPrijavuActionPerformed

