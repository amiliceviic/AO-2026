    protected void popuniTabelu() {
        ModelTabelePregledMojihPrijava mtpmp = new ModelTabelePregledMojihPrijava(lista);
        tabela.setModel(mtpmp);
    }

    private void obrisiFajlKlijenta(PrijavaVolontiranja p) {
        String folder = "prijave_txt";

        String nazivFajla = "prijava_" + p.getIme().toLowerCase() + "_" + p.getPrezime().toLowerCase() + "_" + p.getDatumVolontiranja() + ".txt";

        File fajl = new File(folder, nazivFajla);
        if (fajl.exists()) {
            if (fajl.delete()) {
                System.out.println("Fajl uspešno obrisan: " + fajl.getName());
            } else {
                System.out.println("Greška pri brisanju fajla: " + fajl.getName());
            }
        } else {
            System.out.println("Fajl ne postoji: " + fajl.getName());
        }
    }

