    private void btnPrikaziPrijaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziPrijaveActionPerformed
        // TODO add your handling code here:
        try {
            String jmbg = txtJmbg.getText();
            String email = txtEmail.getText();

            if (jmbg.isEmpty() || email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti JMBG i email!", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Korisnik guest = new Korisnik();
            guest.setJmbg(jmbg);
            guest.setEmail(email);
            
            KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.PREGLED_PRIJAVA, guest);
            
            Komunikacija.getInstance().posaljiZahtev(kz);
            
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            
            if (so.getPoruka().toUpperCase().equals("USPESNO NADJENE PRIJAVE")) {
                lista = (List<PrijavaVolontiranja>) so.getOdgovor();
                popuniTabelu();
            } else if (so.getPoruka().toUpperCase().equals("NEMATE NI JEDNU PRIJAVU")) {
                JOptionPane.showMessageDialog(this, "Greska! Jos uvek nemate prijava!", "Greska", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Greska! Greska pri pregledu prijava!", "Greska", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska pri ucitavanju prijava!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPrikaziPrijaveActionPerformed

