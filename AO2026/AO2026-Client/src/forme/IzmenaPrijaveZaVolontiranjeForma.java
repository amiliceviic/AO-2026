    private void popuniComboBox() {
        for(Smena smena : Smena.values()) {
            cmbSmena.addItem(smena);
        }
        cmbSmena.setSelectedItem(staraPrijava.getSmena());
        
        for(Pozicija pozicija : Pozicija.values()) {
            cmbPozicija.addItem(pozicija);
        }
        cmbPozicija.setSelectedItem(staraPrijava.getPozicija());
    }

    private void popuniDatum() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        txtDatumVolontiranja.setText(p.getDatumVolontiranja().format(formatter));   
    }
    
    private void obrisiFajlKlijenta(PrijavaVolontiranja p) {
        String folder = "prijave_txt";

        String nazivFajla = "prijava_" + p.getIme().toLowerCase() + "_" + p.getPrezime().toLowerCase() + "_" + p.getDatumVolontiranja() + ".txt";

        File fajl = new File(folder, nazivFajla);
        if (fajl.exists()) {
            if (fajl.delete()) {
                System.out.println("Fajl uspešno obrisan: " + fajl.getName());
            } else {
                System.out.println("Greška pri brisanju fajla: " + fajl.getName());
            }
        } else {
            System.out.println("Fajl ne postoji: " + fajl.getName());
        }
    }
