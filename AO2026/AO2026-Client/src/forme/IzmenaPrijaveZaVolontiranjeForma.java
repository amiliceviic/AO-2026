    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        // TODO add your handling code here:
        
        try {
            LocalDate datumVolontiranja = LocalDate.parse(txtDatumVolontiranja.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            p.setDatumVolontiranja(datumVolontiranja);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska! Neispravan format datuma!", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }

        p.setSmena(Smena.valueOf(String.valueOf(cmbSmena.getSelectedItem())));
        p.setPozicija(Pozicija.valueOf(String.valueOf(cmbPozicija.getSelectedItem())));
        
        List<PrijavaVolontiranja> listaStaraNovaPrijava = new ArrayList<>();
        
        listaStaraNovaPrijava.add(staraPrijava);
        listaStaraNovaPrijava.add(p);
        
        obrisiFajlKlijenta(staraPrijava);
        
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.IZMENA_PRIJAVE, listaStaraNovaPrijava);
        
        Komunikacija.getInstance().posaljiZahtev(kz);
        
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        
        if(so.getPoruka().toUpperCase().equals("USPESNO IZMENJENA PRIJAVA")) {
            
            lista.remove(staraPrijava);
            lista.add(p);
            
            byte[] data = (byte[]) so.getOdgovor();

            String naziv = "prijava_" + p.getIme().toLowerCase() + "_" + p.getPrezime().toLowerCase() + "_" + p.getDatumVolontiranja() + "_izmena.txt";

            String folder = "prijave_txt";
            File dir = new File(folder);
            if (!dir.exists()) {
                dir.mkdirs();
            }

            File klijentFajl = new File(dir, naziv);

            try ( FileOutputStream fos = new FileOutputStream(klijentFajl)) {
                fos.write(data);
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Neuspešno čuvanje fajla na klijentu!", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            JOptionPane.showMessageDialog(this, "Uspesno ste se izmenili prijavu za volontiranje na Asutralion Open-u 2026", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
            
            ModelTabelePregledMojihPrijava mtmp = (ModelTabelePregledMojihPrijava) pf.getTabela().getModel();
            mtmp.updateTabelaPrijava();

            this.dispose();
        }
        else {
            List<String> listaGresaka = (List<String>) so.getOdgovor();

            StringBuilder sb = new StringBuilder("Došlo je do sledećih gresaka:\n\n");

            for (String g : listaGresaka) {
                sb.append("- ").append(g).append("\n");
            }

            JOptionPane.showMessageDialog(this, sb.toString(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

