    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if (brojPokusaja < MAX_POKUSAJA - 1) {
            brojPokusaja++;
            String username = txtUsername.getText().trim();
            String password = String.valueOf(passField.getPassword());

            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Greska! Popunite sva polja!", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Korisnik k = new Korisnik(username, password);
            
            KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.LOGIN, k);
            
            Komunikacija.getInstance().posaljiZahtev(kz);

            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            if (so.getPoruka().toUpperCase().equals("USPESNO LOGOVANJE")) {
                ulogovaniKorisnik = (Korisnik) so.getOdgovor();
                JOptionPane.showMessageDialog(this, "Uspesno ste se ulogovali!", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                new KlijentskaForma(ulogovaniKorisnik).setVisible(true);
            } else {
                int preostaliBrojPokusaja = MAX_POKUSAJA - brojPokusaja;
                JOptionPane.showMessageDialog(this, "Greska! Pogresan username ili password! Preostalo pokusaja: " + preostaliBrojPokusaja, "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Greska! Iskoristili ste sve pokusaje!", "Greska", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }

    }//GEN-LAST:event_btnLoginActionPerformed

