    private void btnPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiActionPerformed
        // TODO add your handling code here:
        try {
            PrijavaVolontiranja p = new PrijavaVolontiranja();
            p.setIme(ulogovaniKorisnik.getIme().trim());
            p.setPrezime(ulogovaniKorisnik.getPrezime().trim());
            p.setJmbg(ulogovaniKorisnik.getJmbg().trim());
            p.setEmail(ulogovaniKorisnik.getEmail().trim());

            LocalDate datumVolontiranja = LocalDate.parse(txtDatumVolontiranja.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            p.setDatumVolontiranja(datumVolontiranja);

            p.setSmena(Smena.valueOf(String.valueOf(cmbSmena.getSelectedItem())));
            p.setPozicija(Pozicija.valueOf(String.valueOf(cmbPozicija.getSelectedItem())));

            p.setDatumPrijave(LocalDate.now());

            KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.PRIJAVA_ZA_VOLONTIRANJE, p);

            Komunikacija.getInstance().posaljiZahtev(kz);

            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            
            if (so.getPoruka().toUpperCase().equals("USPESNO STE SE PRIJAVILI")) {
                byte[] data = (byte[]) so.getOdgovor();

                String naziv = "prijava_" + ulogovaniKorisnik.getIme().toLowerCase() + "_" + ulogovaniKorisnik.getPrezime().toLowerCase() + "_" + p.getDatumVolontiranja() + ".txt";

                String folder = "prijave_txt";
                File dir = new File(folder);
                if (!dir.exists()) {
                    dir.mkdirs();
                }

                File klijentFajl = new File(dir, naziv);

                try ( FileOutputStream fos = new FileOutputStream(klijentFajl)) {
                    fos.write(data);
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Neuspešno čuvanje fajla na klijentu!", "Greska", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                JOptionPane.showMessageDialog(this, "Uspesno ste se prijavili za volontiranje na Asutralion Open-u 2026", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
            else {
                List<String> listaGresaka = (List<String>) so.getOdgovor();
                
                StringBuilder sb = new StringBuilder("Došlo je do sledećih gresaka:\n\n");
                
                for(String g: listaGresaka) {
                    sb.append("- ").append(g).append("\n");
                }
                
                JOptionPane.showMessageDialog(this, sb.toString(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska! Greska pri unosu!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPosaljiActionPerformed

