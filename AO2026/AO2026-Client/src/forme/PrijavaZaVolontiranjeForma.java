    private void popuniComboBox() {
        for(Smena smena : Smena.values()) {
            cmbSmena.addItem(String.valueOf(smena));
        }
        cmbSmena.setSelectedIndex(-1);
        
        for(Pozicija pozicija : Pozicija.values()) {
            cmbPozicija.addItem(String.valueOf(pozicija));
        }
        cmbPozicija.setSelectedIndex(-1);
    }
