    private void btnPregledMojihPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPregledMojihPrijavaActionPerformed
        // TODO add your handling code here:
        try {
            KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.PREGLED_PRIJAVA, ulogovaniKorisnik);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            if (so.getPoruka().toUpperCase().equals("USPESNO NADJENE PRIJAVE")) {
                List<PrijavaVolontiranja> lista = (List<PrijavaVolontiranja>) so.getOdgovor();
                new PregledMojihPrijavaForma(lista, ulogovaniKorisnik).setVisible(true);
            } else if(so.getPoruka().toUpperCase().equals("NEMATE NI JEDNU PRIJAVU")) {
                JOptionPane.showMessageDialog(this, "Greska! Jos uvek nemate prijava!", "Greska", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Greska! Greska pri pregledu prijava!", "Greska", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska! Greska u komunikaciji sa serverom!", "Greska", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnPregledMojihPrijavaActionPerformed

